export function getProductById(id: string) {
  return products.find((p) => p.id === id);
}

export function getProductBySlug(slug: string) {
  return products.find((p) => p.slug === slug);
}

export function getProductsByCategory(categoryId: string) {
  return products.filter((p) => p.categoryId === categoryId);
}

export function getProductsByCollection(collectionId: string) {
  return products.filter((p) => p.collectionIds.includes(collectionId));
}

export function searchProducts(query: string) {
  const q = query.toLowerCase();
  return products.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.weight.toLowerCase().includes(q)
  );
}

export function getFeaturedProducts() {
  return products.filter((p) => p.discount).slice(0, 8);
}

export function getTrendingProducts() {
  return [...products].sort((a, b) => b.reviewCount - a.reviewCount).slice(0, 6);
}

export function getRelatedProducts(productId: string, limit = 4) {
  const product = getProductById(productId);
  if (!product) return [];
  return products
    .filter((p) => p.id !== productId && p.categoryId === product.categoryId)
    .slice(0, limit);
}

export function getFrequentlyBoughtTogether(productId: string) {
  const product = getProductById(productId);
  if (!product) return [];
  return products
    .filter((p) => p.id !== productId)
    .sort(() => 0.5 - Math.random())
    .slice(0, 3);
}
